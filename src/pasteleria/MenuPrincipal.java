    public void nombrarBotones(String proviene) {
        if (proviene.equals("Inventario")) {
            boton1.setVisible(true);
            boton1.setText("<html>Administrar<br>productos</html>");
            boton2.setVisible(true);
            boton2.setText("<html>Administrar<br>inventario</html>");
            boton3.setVisible(false);
            boton4.setVisible(false);
            boton5.setVisible(false);
        }
        if (proviene.equals("Informes")) {
            boton1.setVisible(true);
            boton1.setText("Informe de venta");
            boton2.setVisible(true);
            boton2.setText("<html>Informe<br>de Pedidos</html>");
            boton3.setVisible(true);
            boton3.setText("<html>Informe<br>de Inventario</html>");
            boton4.setVisible(true);
            boton4.setText("<html>Informe<br>de Movimiento de Stock</html>");
            boton5.setVisible(true);
            boton5.setText("<html>Informe<br>de anulaciones</html>");

        }
    }

    public void ocultarBotones() {
        boton1.setVisible(false);
        boton2.setVisible(false);
        boton3.setVisible(false);
        boton4.setVisible(false);
        boton5.setVisible(false);
    }

    private void verificarEntradasPermiso(int accesoCaja, int accesoTotal, int accesoPedido) {
        if (accesoTotal == 1) {
            this.accesoTotal = true;
        } else if (accesoCaja == 1) {
            this.accesoCaja = true;
        } else if (accesoPedido == 1) {
            this.accesoPedido = true;
        }
    }

